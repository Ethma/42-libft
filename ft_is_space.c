#include "libft.h"

void	ft_is_space(int *i, int *j, char *s)
{
	while (*i != *j)
	{
		if (s[*i] == ' ' || s[*i] == '\t' || s[*i] == '\n' ||
				s[*j] == ' ' || s[*j] == '\t' || s[*j] == '\n')
			break ;
		i++;
		j--;
	}
}
